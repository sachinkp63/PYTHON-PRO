#==================Operations===================

# 1 => Arithematic Operations...
# Additions(+)
x = 3 
y = 4
print(x + y)
# Subtraction(-)
print(x - y)
# Multiplication(*)
print(x * y)
# Division(/)
print(x / y)
# Modulus(%)
print(x % y)

# 2 => Comparison(Relations) Operations...
# Equal(==)
print(3==4)
# notequal(!=)
print(3 != 4)
# Greater Than
print(3 > 4)
# Less Than
print(3 < 4)
# Greater Than equal
print(3 >= 4)
# Less Than equal
print(3 <= 4)


